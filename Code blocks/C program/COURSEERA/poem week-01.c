#include<stdio.h>
int main()
{
    printf("Daffodils\n");
    printf("By William Wordsworth\n\n");

    printf("I wandered lonely as a cloud\n");
    printf("That floats on high o’er vales and hills,\n");
    printf("When all at once I saw a crowd,\n");
    printf("A host, of golden daffodils;\n");
    printf("Beside the lake, beneath the trees,\n");
    printf("Fluttering and dancing in the breeze.\n\n");

    printf("Continuous as the stars that shine\n");
    printf("twinkle on the milky way,\n");
    printf("They stretched in never-ending line\n");
    printf("Along the margin of a bay:\n");
    printf("Ten thousand saw I at a glance,\n");
    printf("Tossing their heads in sprightly dance.\n");
    return 0;
}
