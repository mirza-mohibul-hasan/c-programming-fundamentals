#include<stdio.h>
int main()
{
    printf("Daffodils\n");
    printf("By William Wordsworth\n\n");

    printf("I wandered lonely as a cloud\n");
    printf("That floats on high o’er vales and hills,\n");
    printf("When all at once I saw a crowd,\n");
    printf("A host, of golden daffodils;\n");
    printf("Beside the lake, beneath the trees,\n");
    printf("Fluttering and dancing in the breeze.\n\n");

    printf("Continuous as the stars that shine\n");
    printf("twinkle on the milky way,\n");
    printf("They stretched in never-ending line\n");
    printf("Along the margin of a bay:\n");
    printf("Ten thousand saw I at a glance,\n");
    printf("Tossing their heads in sprightly dance.\n\n");

    printf("The waves beside them danced; but they\n");
    printf("Out-did the sparkling waves in glee:\n");
    printf("A poet could not but be gay,\n");
    printf("In such a jocund company:\n");
    printf("I gazed—and gazed—but little thought\n");
    printf("What wealth the show to me had brought:\\n\n");

    printf("For oft, when on my couch I lie\n");
    printf("In vacant or in pensive mood,\n");
    printf("They flash upon that inward eye\n");
    printf("Which is the bliss of solitude;\n");
    printf("And then my heart with pleasure fills,\n");
    printf("And dances with the daffodils.\n");
    return 0;
}
